package com.harry;

public class Cwh23_ch5_ForLoop {
    public static void main(String[] args) {
        int n=5;
        for(int i = 0; i<n; i++){
            System.out.print(2*i+1);
        }
        System.out.println();
        int m=5;
        for(int j = m; j!=0; j--){
            System.out.println(j);

        }
    }
}
